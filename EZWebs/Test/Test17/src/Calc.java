
public class Calc {

	public static int Plus(int num1, int num2) {
		return num1 + num2;
	}

	public static int Minus(int num1, int num2) {
		return num1 - num2;
	}

	public static int Mul(int num1, int num2) {
		return num1 * num2;
	}

	public static int Div(int num1, int num2) {
		return num1 / num2;
	}
}

